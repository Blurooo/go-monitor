// 通过上报接口、函数、或者是任意调用服务的耗时以及其成功状态，go-monitor将按照设定的周期自动进行服务质量分析，统计，并输出详细的报告数据。
// 在服务质量达不到理想状态时，go-monitor将触发告警，并在服务质量回升时，触发恢复通知。
